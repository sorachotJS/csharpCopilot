

//make function aad two number if result is greater than 10 then subtract 5 from result
//if result is less than 10 then add 5 to result

int AddTwoNumbers(int num1, int num2)
{
    int result = num1 + num2;
    if (result > 10)
    {
        result = result - 5;
    }
    else
    {
        result = result + 5;
    }
    return result;
}

